n, m = map(int, input().split())
paper = [list(map(int, input().split())) for _ in range(n)]

patterns = [
    ((1, 0), (0, 1), (1, 1)),

    ((1, 0), (2, 0), (3, 0)),
    ((0, 1), (0, 2), (0, 3)),

    ((1, 0), (2, 0), (2, 1)),
    ((1, 0), (2, 0), (2, -1)),
    ((0, 1), (0, 2), (1, 2)),
    ((0, -1), (0, -2), (1, -2)),
    ((0, -1), (1, -1), (2, -1)),
    ((0, 1), (1, 1), (2, 1)),
    ((1, 0), (1, 1), (1, 2)),
    ((1, 0), (1, -1), (1, -2)),

    ((1, 0), (1, 1), (2, 1)),
    ((1, 0), (1, -1), (2, -1)),
    ((0, 1), (1, 1), (1, 2)),
    ((0, -1), (1, -1), (1, -2)),

    ((1, -1), (1, 0), (1, 1)),
    ((-1, -1), (-1, 0), (-1, 1)),
    ((-1, 1), (0, 1), (1, 1)),
    ((-1, -1), (0, -1), (1, -1)),
]


mx = 0
for i in range(n):
    for j in range(m):
        for pattern in patterns:
            point = paper[i][j]
            for x, y in pattern:
                nx, ny = i + x, j + y
                if not (0 <= nx < n and 0 <= ny < m): break
                point += paper[nx][ny]
            else:
                mx = max(mx, point)

print(mx)